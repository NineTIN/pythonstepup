# 자료구조
# 리스트
ex = ['ab','cd','ef'] # ','로 구분
print(ex) # 결과 : ['ab', 'cd', 'ef']

ex = list() # 빈 리스트를 넣을 수도 있다.
print(ex) # 결과 : []

ex = list('cat') # 문자열을 리스트로 변환 할 수도 있다.
print(ex) # 결과 : ['c', 'a', 't']

target = '1919/12/25'
ex = target.split('/') # split를 이용 하는것도 방법
print(ex) # 결과 : ['1919', '12', '25']

