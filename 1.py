# 정수
a = 8
print(a) # 결과 : 8 
print(type(a)) # 결과 : <class 'int'>

# 소수
a = 99.9
print(a) # 결과 : 99.9
print(type(a))  # 결과 : <class 'float'> 

# 문자열
a = 'abcd' # 단행
print(a)
print(type(a))  # 결과 : <class 'str'> 

a = '''abcd
dbcd
''' # 복수행
print(a)
print(type(a))  # 결과 : <class 'str'> 

